    private void SalvarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarBActionPerformed
        AlunoDAO alundao = new AlunoDAO();
        int ls =  tabela.getSelectedRow();
        int id = (int) tabela.getValueAt(ls, 0); 

         try{
             String nome = txtNomeAt.getText().trim();
             String endereco = txtEnderecoAt.getText().trim();
             String cpf = txtCpfAt.getText().trim();
             String curso = txtCursoAt.getText().trim();
             String matricula= txtMatriculaAt.getText().trim();
             int sexo = cbSexoAt.getSelectedIndex();
             String sexotxt = cbSexoAt.getSelectedItem().toString();
             System.out.println("passou");
             if(nome.isEmpty()||endereco.isEmpty()||cpf.isEmpty()||curso.isEmpty()||matricula.isEmpty()||sexo == 0){
                 JOptionPane.showMessageDialog(this, 
                         "Ã‰ necessario preencher todos os campos ou selecionar",
                         "Campos nao preenchidos",
                         JOptionPane.WARNING_MESSAGE);
             }else{
                 Aluno alunoatualizado = new Aluno(curso, matricula, id, nome, endereco, sexotxt, cpf);
                 alundao.atualizarUsuario(alunoatualizado);
                 this.dispose();
             }  
        }catch(SQLException e ){
                 JOptionPane.showMessageDialog(this, 
                         "Erro: "+e.getMessage(),
                         "Erro",
                         JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_SalvarBActionPerformed

